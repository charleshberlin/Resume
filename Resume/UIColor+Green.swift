import UIKit

extension UIColor {
    
    class func resumePrimaryColor() -> UIColor {
        return UIColor(red: 0.19, green: 0.29, blue: 0.10, alpha: 1.0)
    }
    
    class func resumeSecondaryColor() -> UIColor {
        return UIColor(red: 0.16, green: 0.60, blue: 0.04, alpha: 1.0)
    }
    
    class func resumeTextColor() -> UIColor {
        return UIColor.whiteColor()
    }
}
